#Use a variable to represent a person’s name, and then print that person’s name in lowercase, uppercase, and title case.
name = "hugh Michael jackman " #Fun fact, his middle name really is Michael!

#Lowercase
print("Name in all lowercase:")
print(f"\t{name.lower()}")

#Uppercase
print("Name in all uppercase:")
print(f"\t{name.upper()}")

#Title
print("Name with first letters capitalized:")
print(f"\t{name.title()}")